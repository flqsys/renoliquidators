<?php
namespace BeRocket\EngagementEngine;

class DataLoader {
	private array $loaded_data = [];

	public function load() {
		$this->pull_data();
		$this->fill_locked_features();
		$this->fill_smart_triggers();
		$this->fill_notices();
		$this->fill_news();
		$this->fill_banners();
		$this->save_data();
	}

	private function pull_data(): void {
		$url  = 'https://apicdn.berocket.com/ee';
		$data = @file_get_contents( $url );

		if ( $data !== false ) {
			$json = json_decode( $data, true );
			if ( json_last_error() === JSON_ERROR_NONE ) {
				$this->loaded_data = $this->validate_data( $json );
			}
		}
	}

	private function fill_locked_features(): void {
		if ( $this->loaded_data and ! empty( $this->loaded_data['locked_features'] ) ) {
			$this->loaded_data['locked_features'] = ( new LockedFeatures() )->validate_data( $this->loaded_data['locked_features'] );
		}
	}

	private function fill_smart_triggers(): void {
	}

	private function fill_notices(): void {
	}

	private function fill_news(): void {
	}

	private function fill_banners(): void {
	}

	private function save_data(): void {
		update_option( BR_EE_OPTION, $this->loaded_data );
	}

	private function validate_data( $data = [] ): array {
		// no other features allowed except the BR_EE_LIST
		$new_data = [];
		foreach ( BR_EE_LIST as $ee ) {
			if ( empty( $data[ $ee ] ) )
				$new_data[ $ee ] = [];
			else
				$new_data[ $ee ] = $data[ $ee ];
		}

		return $new_data;
	}

